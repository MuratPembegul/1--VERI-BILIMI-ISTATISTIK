""" ORTALAMA NEDİR? 
Ortalama, bir veri setindeki tüm değerlerin toplamının, 
o veri setindeki eleman sayısına bölünmesiyle elde edilen merkezi bir ölçüdür. 
İstatistikte ortalama, genellikle veri setinin genel eğilimini veya 
merkezini göstermek için kullanılır. 
Aritmetik ortalama, yaygın olarak kullanılan en basit ortalama türüdür 
ve çoğu durumda "ortalama" denildiğinde aritmetik ortalama kastedilir.
"""
# Bir veri seti tanımlayalım
veri = [10, 20, 30, 40, 50]

# Ortalama hesaplayalım
ortalama = sum(veri) / len(veri)

# Sonucu ekrana yazdıralım
print("Veri setinin ortalaması:", ortalama)

Sonuç: 30.0
